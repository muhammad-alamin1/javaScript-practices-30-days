// Arithmetic Operator
// +, -, *, /, %, ++, -- 
var a = 10;
var b = 3;

// console.log(a % b);

/**
    ++  ---> Incremental
    --  ---> Decremental
 */
console.log(a++); // Post increment
console.log(a);
console.log(++a); // Pre increment
console.log(--b); // Pre decrement
console.log(b--); // Post decrement
console.log(b);

// Assignment Operator
var aa = 10;
var bb = 20;

aa += bb;
console.log(aa);

aa -= bb;
console.log(aa);
// or Operator *=, /=, %=

// Comparison Operator // Comparison operator always return boolean value 
// ==, !=
var x = 10;
var y = 20;

console.log(x == y);  // false                                                                  
console.log(x != y); // true

console.log(typeof (''));

